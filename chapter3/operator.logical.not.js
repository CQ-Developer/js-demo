// 逻辑非
console.log('!false =', !false);
console.log('!"blue" =', !'blue');
console.log('!0 =', !0);
console.log('!NaN =', !NaN);
console.log('!"" =', !'');
console.log('!null =', !null);
console.log('!12345 =', !12345);
console.log('!undefined =', !undefined);

// !! 等于 Boolean()
console.log('!!false =', !!false);
console.log('!!"blue" =', !!'blue');
console.log('!!0 =', !!0);
console.log('!!NaN =', !!NaN);
console.log('!!"" =', !!'');
console.log('!!null =', !!null);
console.log('!!12345 =', !!12345);
console.log('!!undefined =', !!undefined);
