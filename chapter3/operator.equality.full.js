// 相等操作符
// 全等和不全等

let n1 = '55' == 55;
console.log(`'55' == 55 = ${n1}`);

let n2 = '55' === 55;
console.log(`'55' === 55 = ${n2}`);

let n3 = '55' != 55;
console.log(`'55' != 55 = ${n3}`);

let n4 = '55' !== 55;
console.log(`'55' !== 55 = ${n4}`);

let n5 = null == undefined;
console.log(`null == undefined = ${n5}`);

let n6 = null === undefined;
console.log(`null === undefined = ${n6}`);
