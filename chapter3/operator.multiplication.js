// 乘法操作符

let result = 34 * 56;
console.log('34 * 56 =', result);

let n1 = -23 * -33;
console.log('-23 * -33 = ', n1);

let n2 = Number.MAX_VALUE * 2;
console.log('Number.MAX_VALUE * 2 =', n2);

let n3 = NaN * 3;
console.log('NaN * 3 =', n3);

let n4 = Infinity * 0;
console.log('Infinity * 0 =', n4);

let n5 = Infinity * 1;
console.log('Infinity * 1 =', n5);

let n6 = Infinity * -1;
console.log('Infinity * -1 =', n6);

let n7 = n2 * n2;
console.log('Infinity * Infinity =', n7);

let n8 = true * 33;
console.log('true * 33 =', n8);

let n9 = false * 33;
console.log('false * 33 =', n9);

let n10 = '' * 33;
console.log('"" * 33 =', n10);

let n11 = '2' * 33;
console.log('"2" * 33 =', n11);

let n12 = null * 22;
console.log('null * 22 =', n12);
