// 赋值操作符

let n1 = 10
n1 += 10;
console.log(`10 += 10 = ${n1}`);

let n2 = 3;
n2 *= 4;
console.log(`3 *= 4 = ${n2}`);

let n3 = 8;
n3 /= 2;
console.log(`8 /= 2 = ${n3}`);

let n4 = 9;
n4 %= 2;
console.log(`9 %= 2 = ${n4}`);

let n5 = 13;
n5 -= 10;
console.log(`13 -= 10 = ${n5}`);

let n6 = 64;
n6 <<= 1;
console.log(`64 <<= 1 = ${n6}`);

let n7 = 64;
n7 >>= 1;
console.log(`64 >>= 1 = ${n7}`);

let n8 = 64;
n8 >>>= 2;
console.log(`64 >>>= 1 = ${n8}`);
